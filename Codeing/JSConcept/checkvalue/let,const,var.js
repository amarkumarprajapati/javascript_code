// var is function-scoped and allows both re-declaration and re-assignment. 
// let is block-scoped, allows re-assignment but not re-declaration. 
// const is block-scoped and allows neither re-declaration nor re-assignment. 
// While all three are hoisted, let and const are placed in the Temporal Dead Zone until their declaration is executed.


// console.log(a)
// var a = 10

// console.log(b)
// let b = 20

